    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String valorEuro = cteuro.getText();
        if (valorEuro.equals("")) {
            JOptionPane.showMessageDialog(null, "formato no valido");
        } else {
            double valorEuroO = Double.parseDouble(valorEuro);
            finalEuro = valorEuroO * euro;
            //muestro en el label
            lbleuro.setText("son " + finalEuro + " pesos Colombianos");
            calcularTotal();
        }
        
    }                                        

