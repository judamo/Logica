    private void btnDolarActionPerformed(java.awt.event.ActionEvent evt) {                                         

        String valorDolar = ctdolar.getText();
        if (valorDolar.equals("")) {
            JOptionPane.showMessageDialog(null, "formato no valido");
        } else {
            double valorDolarO = Double.parseDouble(valorDolar);
            finalDolar = valorDolarO * dolar;
            //muestro en el label
            lbldolar.setText("son " + finalDolar + " pesos Colombianos");
            
            calcularTotal();
           
            
        }


    }                                        

